
public class MathClass {

	public static void main(String[] args) {
		//Doing math class
		System.out.println("Absolute value: " + Math.abs(-7.1));
		System.out.println("Floor price: " + Math.floor(2.6));
		System.out.println("Ceil price: " + Math.ceil(7.9));
		System.out.println("Max number: " + Math.max(10.1, 4.2));
		System.out.println("Min number: " + Math.min(2.3, 1.0));
		System.out.println("Power of number: " + Math.pow(2 , 5));
		System.out.println("Square root: " + Math.sqrt(10.5));
		

	}

}
